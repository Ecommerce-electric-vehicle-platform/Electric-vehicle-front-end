// Hàm định dạng tiền VNĐ
export const formatCurrency = (value) => {
    return value.toLocaleString("vi-VN") + " ₫";
};

// Hàm định dạng ngày
export const formatDate = (dateString) => {
    const date = new Date(dateString);
    return date.toLocaleDateString("vi-VN");
};
